function getPilot( rom, index, wave, i )
{
	let hexPilot=rom[(index[0][wave]+(i*8))];
	return stringPilot[hexPilot];
};

function getMech( rom, index, wave, i)
{
	let hexMech=rom[(index[2][wave]+(i*8))];
	return stringMechs[hexMech];
};